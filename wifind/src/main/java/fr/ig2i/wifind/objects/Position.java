public class Position {

    /**
     * Position X
     */
    private float x;

    /**
     * Position Y
     */
    private float y;



    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
